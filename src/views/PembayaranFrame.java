    private int hitungDenda(String idSewa) {
    try {
        String sql = "SELECT tgl_jatuh_tempo, k.harga_per_bulan FROM sewa s JOIN kamar k ON s.no_kamar = k.no_kamar WHERE s.id_sewa = " + idSewa;
        java.sql.Connection conn = config.koneksi.configDB();
        java.sql.ResultSet res = conn.createStatement().executeQuery(sql);

        if (res.next()) {
            java.util.Date jatuhTempo = res.getDate("tgl_jatuh_tempo");
            int hargaKamar = res.getInt("harga_per_bulan");

            // --- BARU: Format Tanggal dan Tampilkan ---
            java.text.SimpleDateFormat formatter = new java.text.SimpleDateFormat("dd-MM-yyyy");
            txtJatuhTempo.setText(formatter.format(jatuhTempo));
            // ------------------------------------------

            java.util.Date hariIni = new java.util.Date();
            long diff = hariIni.getTime() - jatuhTempo.getTime();
            long selisihHari = diff / (24 * 60 * 60 * 1000);

            if (selisihHari > 0) {
                int dendaPerHari = 10000;
                int totalDenda = (int) (selisihHari * dendaPerHari);

                // Tampilkan denda dan total bayar
                txtDenda.setText(String.valueOf(totalDenda));
                txtTotalBayar.setText(String.valueOf(hargaKamar + totalDenda));
                return totalDenda;
            } else {
                // Tampilkan 0 jika tidak ada denda
                txtDenda.setText("0");
                txtTotalBayar.setText(String.valueOf(hargaKamar));
                return 0;
            }
        } else {
            // Jika ID Sewa tidak ditemukan
            txtDenda.setText("");
            txtTotalBayar.setText("");
            txtJatuhTempo.setText("");
            return 0;
        }
        } catch (Exception e) {
        System.err.println("Error hitung denda: " + e.getMessage());

        // --- PERBAIKAN: Reset fields di sini ---
        txtDenda.setText("");
        txtTotalBayar.setText("");
        txtJatuhTempo.setText("");
        // ------------------------------------

        return 0;
    }
}
    
    
    private void tampilNamaPenghuni() {
    try {
        java.sql.Connection conn = config.koneksi.configDB();
        
        // 1. OTOMATISASI STATUS: UPDATE status menjadi 'Belum Dibayar'
        //    Jika tgl_jatuh_tempo sudah lewat dari hari ini DAN statusnya masih 'Sudah Dibayar'
        String sqlCekStatus = "UPDATE sewa SET status_pembayaran = 'Belum Dibayar' WHERE tgl_jatuh_tempo < CURRENT_DATE() AND status_pembayaran = 'Sudah Dibayar'";
        conn.createStatement().executeUpdate(sqlCekStatus);
        
        // 2. Muat data NAMA Penghuni yang sedang aktif
        String sql = "SELECT s.id_sewa, p.nama_penghuni FROM sewa s JOIN penghuni p ON s.ktp_penghuni = p.ktp_penghuni";
        java.sql.Statement stm = conn.createStatement();
        java.sql.ResultSet res = stm.executeQuery(sql);

        cbSewa.removeAllItems();
        cbSewa.addItem("-- Pilih Nama Penghuni --"); // Ganti teks default

        while (res.next()) {
            cbSewa.addItem(res.getString("nama_penghuni"));
        }
    } catch (Exception e) {
        System.err.println("Error load Nama Penghuni: " + e.getMessage());
    }
}
    
