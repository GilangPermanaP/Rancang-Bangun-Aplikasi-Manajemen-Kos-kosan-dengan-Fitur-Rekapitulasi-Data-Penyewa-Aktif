    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        // TODO add your handling code here:
        if (idSewaTerpilih == null) {
        JOptionPane.showMessageDialog(this, "Pilih Penghuni terlebih dahulu.");
        return;
    }
    
    int konfirmasi = JOptionPane.showConfirmDialog(null, "Konfirmasi Pembayaran?", "Bayar", JOptionPane.YES_NO_OPTION);

    if (konfirmasi == JOptionPane.YES_OPTION) {
        try {
            // UPDATE: Set status ke 'Sudah Dibayar', perbarui tgl_jatuh_tempo (+1 bulan), dan tgl_bayar_terakhir (hari ini)
            String sqlUpdate = "UPDATE sewa SET tgl_jatuh_tempo = DATE_ADD(tgl_jatuh_tempo, INTERVAL 1 MONTH), tgl_bayar_terakhir = CURRENT_DATE(), status_pembayaran = 'Sudah Dibayar' WHERE id_sewa = ?";
            
            java.sql.Connection conn = config.koneksi.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sqlUpdate);
            pst.setString(1, idSewaTerpilih); // Gunakan ID Sewa yang disimpan dari ComboBox
            
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Pembayaran Berhasil Dicatat! Status: Sudah Dibayar.");
            
            // Refresh tampilan setelah bayar
            txtDenda.setText("");
            txtTotalBayar.setText("");
            txtJatuhTempo.setText("");
            tampilNamaPenghuni(); // Refresh ComboBox
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal Mencatat Pembayaran: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnBayarActionPerformed

    private void cbSewaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbSewaItemStateChanged
        // TODO add your handling code here:
        if (evt.getStateChange() == java.awt.event.ItemEvent.SELECTED) {
        String namaTerpilih = cbSewa.getSelectedItem().toString();
        
        if (!namaTerpilih.contains("--")) {
            try {
                // Cari ID Sewa berdasarkan Nama yang dipilih
                String sql = "SELECT s.id_sewa FROM sewa s JOIN penghuni p ON s.ktp_penghuni = p.ktp_penghuni WHERE p.nama_penghuni='" + namaTerpilih + "'";
                java.sql.Connection conn = config.koneksi.configDB();
                java.sql.ResultSet res = conn.createStatement().executeQuery(sql);
                
                if (res.next()) {
                    idSewaTerpilih = res.getString("id_sewa"); // Simpan ID Sewa
                    hitungDenda(idSewaTerpilih); // Gunakan ID Sewa yang disimpan
                }
            } catch (Exception e) {
                System.err.println("Error saat memilih Penghuni: " + e.getMessage());
            }
        } else {
            idSewaTerpilih = null;
            txtDenda.setText("");
            txtTotalBayar.setText("");
            txtJatuhTempo.setText("");
        }
    }
    }//GEN-LAST:event_cbSewaItemStateChanged

