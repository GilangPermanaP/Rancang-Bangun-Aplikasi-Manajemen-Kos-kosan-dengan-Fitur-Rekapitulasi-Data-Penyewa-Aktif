    private void tampilKamar() {
        try {
            String sql = "SELECT no_kamar FROM kamar WHERE status='Kosong'";
            java.sql.Connection conn = (java.sql.Connection) config.koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);

            // --- Perbaikan di sini ---
            cbKamar.removeAllItems(); // Bersihkan item lama
            cbKamar.addItem("-- Pilih Kamar --"); // Tambahkan default item
            // --- Akhir Perbaikan ---

            while(res.next()) {
                cbKamar.addItem(res.getString("no_kamar"));
            }
        } catch (Exception e) {
            System.out.println("Error Load ComboBox Kamar: " + e.getMessage());
        }
    }
    
    private void tampilPenghuni() {
        try {
            String sql = "SELECT nama_penghuni FROM penghuni";
            java.sql.Connection conn = (java.sql.Connection) config.koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);

            cbPenghuni.removeAllItems();
            cbPenghuni.addItem("-- Pilih Penghuni --");

            while(res.next()) {
                cbPenghuni.addItem(res.getString("nama_penghuni"));
            }
        } catch (Exception e) {
            System.out.println("Error Load ComboBox Penghuni: " + e.getMessage());
        }
    }
        
    private void loadDataSewa() {
    javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
    model.addColumn("ID Sewa");
    model.addColumn("No. Kamar");
    model.addColumn("Nama Penghuni");
    model.addColumn("Tgl Masuk");
    model.addColumn("Status Bayar"); // <<< Kolom Baru Ditambahkan

    try {
        // Menggunakan JOIN dan mengambil kolom status_pembayaran
        String sql = "SELECT s.id_sewa, s.no_kamar, p.nama_penghuni, s.tgl_masuk, s.status_pembayaran FROM sewa s JOIN penghuni p ON s.ktp_penghuni = p.ktp_penghuni";
        java.sql.Connection conn = (java.sql.Connection) config.koneksi.configDB();
        java.sql.Statement stm = conn.createStatement();
        java.sql.ResultSet res = stm.executeQuery(sql);

        while(res.next()) {
            // Kita harus mengambil 5 kolom sekarang
            model.addRow(new Object[]{res.getString(1), res.getString(2), res.getString(3), res.getString(4), res.getString(5)});
        }
            tblSewa.setModel(model);
        } catch (Exception e) {
            System.out.println("Error Load Data Sewa: " + e.getMessage());
        }
    }
    
