    private void btnCheckInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckInActionPerformed
        // TODO add your handling code here:
        // Tambahkan Validasi dasar
    if (cbKamar.getSelectedItem().toString().contains("--") || cbPenghuni.getSelectedItem().toString().contains("--") || idTerpilih == null) {
        JOptionPane.showMessageDialog(this, "Pilih Kamar dan Penghuni terlebih dahulu.");
        return;
    }

    try {
        java.sql.Connection conn = (java.sql.Connection) config.koneksi.configDB();

        // 1. Query INSERT dengan inisialisasi tgl_jatuh_tempo dan status_pembayaran
        String sqlSewa = "INSERT INTO sewa (no_kamar, ktp_penghuni, tgl_masuk, tgl_jatuh_tempo, tgl_bayar_terakhir, status_pembayaran) "
                       + "VALUES (?, ?, ?, DATE_ADD(?, INTERVAL 1 MONTH), ?, 'Belum Dibayar')";

        // 2. Update status kamar
        String sqlUpdate = "UPDATE kamar SET status='Terisi' WHERE no_kamar=?";

        // --- EKSEKUSI TRANSAKSI ---

        // Eksekusi Simpan Sewa
        java.sql.PreparedStatement pstSewa = conn.prepareStatement(sqlSewa);
        pstSewa.setString(1, cbKamar.getSelectedItem().toString());
        pstSewa.setString(2, idTerpilih); // KTP Penghuni
        pstSewa.setString(3, txtTglMasuk.getText()); // tgl_masuk
        pstSewa.setString(4, txtTglMasuk.getText()); // TGL MASUK (untuk DATE_ADD)
        pstSewa.setString(5, txtTglMasuk.getText()); // tgl_bayar_terakhir
        pstSewa.executeUpdate();

        // Eksekusi Update Kamar
        java.sql.PreparedStatement pstUpdate = conn.prepareStatement(sqlUpdate);
        pstUpdate.setString(1, cbKamar.getSelectedItem().toString());
        pstUpdate.executeUpdate();

        // NOTIFIKASI BERHASIL (Muncul setelah semua SQL sukses)
        JOptionPane.showMessageDialog(null, "Check-in Berhasil! Kamar " + cbKamar.getSelectedItem() + " kini terisi.");

        // Refresh Data Tabel Sewa dan ComboBox Kamar
        loadDataSewa();
        tampilKamar();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal Check-In: " + e.getMessage());
    }
    }//GEN-LAST:event_btnCheckInActionPerformed

    private void cbPenghuniItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbPenghuniItemStateChanged
        // TODO add your handling code here:
        if (evt.getStateChange() == java.awt.event.ItemEvent.SELECTED) {
            try {
                String namaTerpilih = cbPenghuni.getSelectedItem().toString();

                // Mencari KTP berdasarkan Nama yang dipilih
                String sql = "SELECT ktp_penghuni FROM penghuni WHERE nama_penghuni='" + namaTerpilih + "'";
                java.sql.Connection conn = (java.sql.Connection) config.koneksi.configDB();
                java.sql.Statement stm = conn.createStatement();
                java.sql.ResultSet res = stm.executeQuery(sql);

                if (res.next()) {
                    idTerpilih = res.getString("ktp_penghuni"); // Variabel global idTerpilih terisi di sini
                }
            } catch (Exception e) {
                System.err.println("Error saat memilih Penghuni: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_cbPenghuniItemStateChanged

    private void btnCheckOutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckOutActionPerformed
        // TODO add your handling code here:
        if (idSewaTerpilih == null) {
        JOptionPane.showMessageDialog(this, "Silakan pilih data sewa yang akan di Check-Out dari tabel.");
        return;
    }

    int konfirmasi = JOptionPane.showConfirmDialog(null,
                "Yakin ingin Check-Out kamar " + noKamarTerpilih + "?", "Konfirmasi", JOptionPane.YES_NO_OPTION);

    if (konfirmasi == JOptionPane.YES_OPTION) {
        try {
            // 1. Query DELETE dari tabel sewa
            String sqlDelete = "DELETE FROM sewa WHERE id_sewa='" + idSewaTerpilih + "'";

            // 2. Query UPDATE status kamar menjadi 'Kosong'
            String sqlUpdate = "UPDATE kamar SET status='Kosong' WHERE no_kamar='" + noKamarTerpilih + "'";

            java.sql.Connection conn = (java.sql.Connection) config.koneksi.configDB();

            // Eksekusi Hapus Sewa
            java.sql.PreparedStatement pstDelete = conn.prepareStatement(sqlDelete);
            pstDelete.execute();

            // Eksekusi Update Kamar
            java.sql.PreparedStatement pstUpdate = conn.prepareStatement(sqlUpdate);
            pstUpdate.execute();

            // NOTIFIKASI BERHASIL
            JOptionPane.showMessageDialog(null, "Check-Out Berhasil! Kamar " + noKamarTerpilih + " kini KOSONG.");

            // Refresh tampilan
            loadDataSewa();
            tampilKamar();

            // Reset variabel terpilih
            idSewaTerpilih = null;
            noKamarTerpilih = null;

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error Check-Out: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnCheckOutActionPerformed

